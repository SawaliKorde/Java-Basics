package com.company;

public class resulting_data_type {
    public static void main(String[] args) {
        byte x = 5;
        int y = 6;
        short z = 8;
        int a = y + z;
        float b = 1 + 6.5f;
        System.out.println(b);

        //Increment and Decrement Operators
        int i = 56;
        System.out.println(i++); //first use value then increment
        System.out.println(i);
        System.out.println(++i); //first increment then use it
        System.out.println(i);
    }
}
